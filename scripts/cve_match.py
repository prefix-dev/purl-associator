"""Match conda-forge packages to OSV advisories via their PURL mapping.

For each package with at least one PURL in ``web/public/mappings.json``
(generated by :mod:`scripts.merge_mappings`), this script:

1. Looks up OSV advisories whose ``affected[].package`` references that PURL
   (or any of its alternatives).
2. Enumerates every conda-forge version of the package via
   ``rattler.repo_data.Gateway``.
3. Intersects each advisory's affected-version *ranges* (or explicit version
   list) with the set of conda-forge versions, producing the set of conda
   versions believed to be vulnerable.
4. Writes one JSON file per package to ``mappings/cves/<pkg>.json``.

The version comparison defaults to a direct upstream-vs-conda match using
``rattler.version.Version`` (which understands both PEP 440-ish PyPI versions
and conda's epoch/local extensions). Manual *version overrides* recorded in
``mappings/cve_contributions/`` flip individual versions when this default
mistranslates — :mod:`scripts.merge_cves` applies those during the merge.
"""

